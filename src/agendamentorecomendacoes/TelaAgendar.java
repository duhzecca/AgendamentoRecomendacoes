    private void btSelecionaAlunoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_btSelecionaAlunoFocusLost
        // TODO add your handling code here:
         try{
            alunos = ConexaoBD.getInstance().buscaTempAlunos();            
            for(Aluno a : alunos){
                model.addElement(a.getNome());                
            }                        
            ltAluno.setModel(model);            
        }catch(SQLException sqle){
            Logger.getLogger(TelaSelecionarObj.class.getName()).log(Level.SEVERE, null, sqle);
        } 
    }//GEN-LAST:event_btSelecionaAlunoFocusLost

