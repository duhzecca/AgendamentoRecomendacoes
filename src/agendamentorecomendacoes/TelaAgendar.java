    private void btAgendarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btAgendarMouseClicked
        // TODO add your handling code here:
    
        
        f = new ObjRecomendacao();
        f.setTitulo(tbTitulo.getText());
        f.setTema(tbTema.getText());
        f.setTurma(tbTurma.getText());
        Date d =  new Date();
        DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        d = tbData.getDate();   
        if(d == null){
            f.setDataAgenda("");
        }else{
            f.setDataAgenda(df.format(d));  
        }
        f.setObservacao(tbObs.getText());
        if(frag != false){
            String a = ltAluno.getSelectedValue().toString();
            f.setAluno(a);            
        }
        f.setObjAprendizagem(tbArquivo.getText());        
        
        if(f.getTitulo().equals("") || f.getTema().equals("") || f.getDataAgenda().equals("") || f.getTurma().equals("") || f.getAluno().equals("")
                || f.getObjAprendizagem().equals("")){
            JOptionPane.showMessageDialog(null, "Titulo, Tema, Data, Turma, Aluno ou Objeto estão nulos. Preencha, por favor.");
        }else{            
            JOptionPane.showMessageDialog(null, "Agendamento realizado com sucesso");
            ConexaoBD rs = new ConexaoBD();
            try {
                rs.insereRecomendacao(f.getTitulo(), f.getTema(), f.getDataAgenda(),f.getAluno(),f.getObjAprendizagem(), f.getTurma());
            } catch (SQLException ex) {
                Logger.getLogger(TelaAgendar.class.getName()).log(Level.SEVERE, null, ex);
            }
            f.setObservacao("");
            f.setTema("");
            f.setTitulo("");
            f.setDataAgenda("");
            f.setObjAprendizagem("");
            f.setTurma("");
            tbTitulo.setText("");
            tbTema.setText("");
            tbData.setDate(null);
            tbObs.setText("");
            tbTurma.setText("");
            tbArquivo.setText("Adicione um arquivo a recomendar");
            ltAluno.setModel(new DefaultListModel());
             //APAGAR TODOS OS ITEMS da tabela temp aqui
            try{
                    ConexaoBD.getInstance().deleteTemp();
                    ConexaoBD.getInstance().deleteTempObj();
                }catch(SQLException sqle){
                    Logger.getLogger(TelaSelecionarObj.class.getName()).log(Level.SEVERE, null, sqle);

            }
        }
       
           
        
        
        
    }//GEN-LAST:event_btAgendarMouseClicked

